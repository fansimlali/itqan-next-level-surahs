export const getNextGrade = (currentGrade) => {
  if (!currentGrade) return null;
  return GRADE_PROGRESSION[currentGrade] || null;
};

export const isLastGrade = (grade) => {
  return getNextGrade(grade) === null && grade !== null;
};

export const isValidGrade = (grade) => {
  return grade in GRADE_PROGRESSION;
};